class Solution {
  public:
    long long maxTripletProduct(long long v[], int n)
    {
    	// Complete the function
    	sort(v, v + n);
    	return max(1ll * (v[0] * v[1] * v[n - 1]), 1ll * v[n - 1] * v[n - 2] * v[n - 3]);
    }
};

